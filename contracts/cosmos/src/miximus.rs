// =========================================================================
//                 BN254 FIELD ARITHMETIC (using Uint256)
// =========================================================================

/// Parse the BN254 scalar field modulus from its decimal string representation.
fn scalar_field() -> Uint256 {
    Uint256::from_str_prefixed(SCALAR_FIELD_STR).unwrap()
}

/// Parse a decimal string into a Uint256. Used for level IVs and leaf hashes.
fn uint256_from_dec(s: &str) -> Uint256 {
    Uint256::from_str_prefixed(s).unwrap()
}

/// Convert a Uint256 to a 32-byte big-endian representation.
fn uint256_to_bytes(v: &Uint256) -> Vec<u8> {
    // Uint256 stores as big-endian 32 bytes
    let be = v.to_be_bytes();
    be.to_vec()
}

/// Convert 32-byte big-endian bytes to Uint256.
fn bytes_to_uint256(b: &[u8]) -> Uint256 {
    let mut buf = [0u8; 32];
    if b.len() >= 32 {
        buf.copy_from_slice(&b[..32]);
    } else {
        // Left-pad with zeros
        let offset = 32 - b.len();
        buf[offset..].copy_from_slice(b);
    }
    Uint256::from_be_bytes(buf)
}

/// Modular addition: (a + b) mod p
fn addmod(a: Uint256, b: Uint256, p: Uint256) -> Uint256 {
    // Use Uint512 approach: compute in wider space to avoid overflow
    // Uint256::checked_rem handles the mod. We need overflow-safe add.
    // cosmwasm Uint256 wraps on overflow, so we use the formula:
    // if a + b overflows or a + b >= p, result = a + b - p (possibly twice)
    // But Uint256 can hold up to 2^256 - 1, and our field is ~254 bits,
    // so a < p and b < p means a + b < 2p < 2^255, which fits in Uint256.
    let sum = a + b;
    if sum >= p {
        sum - p
    } else {
        sum
    }
}

/// Modular multiplication: (a * b) mod p
/// Since Uint256 * Uint256 can overflow 256 bits, we implement this using
/// the double-and-add (Russian peasant) method on Uint256.
fn mulmod(a: Uint256, b: Uint256, p: Uint256) -> Uint256 {
    if a.is_zero() || b.is_zero() {
        return Uint256::zero();
    }
    if p.is_zero() {
        return Uint256::zero();
    }

    let a = a % p;
    let b = b % p;

    // Use schoolbook multiplication via double-and-add to avoid overflow.
    // Process bits of b from MSB to LSB.
    let b_bytes = b.to_be_bytes();
    let mut result = Uint256::zero();
    let mut base = a;

    // Iterate bits from MSB to LSB
    for byte_idx in 0..32 {
        let byte_val = b_bytes[byte_idx];
        for bit_idx in (0..8).rev() {
            // Double
            result = addmod(result, result, p);
            // Add if bit is set
            if (byte_val >> bit_idx) & 1 == 1 {
                result = addmod(result, base, p);
            }
        }
    }
    let _ = base; // suppress unused warning

    result
}

// =========================================================================
//                    MiMC HASH FUNCTION (BN254 field)
// =========================================================================

/// Compute keccak256 of a 32-byte input, returning 32 bytes.
fn keccak256(data: &[u8]) -> [u8; 32] {
    let mut hasher = Keccak::v256();
    let mut output = [0u8; 32];
    hasher.update(data);
    hasher.finalize(&mut output);
    output
}

/// Generate MiMC round constants using a keccak256 hash chain.
/// seed = keccak256("mimc"), then c[i] = keccak256(c[i-1])
/// Returns 91 round constants as Uint256 values reduced mod SCALAR_FIELD.
fn mimc_round_constants() -> Vec<Uint256> {
    let p = scalar_field();
    let seed = keccak256(b"mimc");
    let mut constants = Vec::with_capacity(MIMC_ROUNDS);
    let mut current = seed;

    for _ in 0..MIMC_ROUNDS {
        current = keccak256(&current);
        let c = bytes_to_uint256(&current) % p;
        constants.push(c);
    }

    constants
}

/// MiMC-p/p cipher with exponent 7, matching the ethsnarks C++ circuit.
///
/// For each of 91 rounds:
///   t = x + c_i + k  (mod p)
///   x = t^7           (mod p)
/// Final: output = x + k  (mod p)
///
/// Where c_i are keccak256 hash-chain round constants and p is the BN254 scalar field.
fn mimc_cipher(in_x: Uint256, in_k: Uint256) -> Uint256 {
    let p = scalar_field();
    let constants = mimc_round_constants();
    let mut x = in_x % p;
    let k = in_k % p;

    for i in 0..MIMC_ROUNDS {
        // t = x + c_i + k (mod p)
        let t = addmod(addmod(x, constants[i], p), k, p);

        // t^7 = t * (t^2)^3
        let t2 = mulmod(t, t, p);       // t^2
        let t4 = mulmod(t2, t2, p);     // t^4
        let t6 = mulmod(t4, t2, p);     // t^6
        x = mulmod(t6, t, p);           // t^7
    }

    // Final key addition
    addmod(x, k, p)
}

/// MiMC hash using Miyaguchi-Preneel compression, matching ethsnarks MiMC.Hash().
///
/// r starts at iv. For each input x:
///   h = mimc_cipher(x, r)
///   r = r + x + h  (mod p)
///
/// This is the standard Miyaguchi-Preneel construction applied to MiMC.
fn mimc_hash(data: &[Uint256], iv: Uint256) -> Uint256 {
    let p = scalar_field();
    let mut r = iv;

    for x in data {
        let h = mimc_cipher(*x, r);
        r = addmod(addmod(r, *x, p), h, p);
    }

    r
}

/// Convenience: MiMC hash with IV = 0 (used for nullifier hash, pub_hash, leaf hash).
fn mimc_hash_default(data: &[Uint256]) -> Uint256 {
    mimc_hash(data, Uint256::zero())
}

/// MiMC hash with a level-specific IV, used for Merkle tree level hashing.
fn mimc_hash_with_level_iv(data: &[Uint256], level: usize) -> Uint256 {
    let iv = uint256_from_dec(LEVEL_IVS[level]);
    mimc_hash(data, iv)
}

