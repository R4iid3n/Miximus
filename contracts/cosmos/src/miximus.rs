#[entry_point]
pub fn instantiate(
    deps: DepsMut,
    _env: Env,
    info: MessageInfo,
    msg: InstantiateMsg,
) -> StdResult<Response> {
    let oracle_addr = deps.api.addr_validate(&msg.oracle)?;

    let config = Config {
        owner: info.sender.clone(),
        oracle: oracle_addr,
        denomination: msg.denomination,
        denom: msg.denom,
        asset_symbol: msg.asset_symbol,
        vk_data: msg.vk_data,
    };
    CONFIG.save(deps.storage, &config)?;

    // Initialize the Merkle tree: compute zero hashes for each level and store
    let initial_root = initialize_merkle_tree(deps)?;

    // Store initial root as valid
    ROOTS.save(deps.storage, initial_root.clone(), &true)?;
    CURRENT_ROOT.save(deps.storage, &initial_root)?;
    NEXT_LEAF_INDEX.save(deps.storage, &0u64)?;

    Ok(Response::new()
        .add_attribute("method", "instantiate")
        .add_attribute("owner", info.sender)
        .add_attribute("denomination", msg.denomination))
}

#[entry_point]
pub fn execute(
    deps: DepsMut,
    env: Env,
    info: MessageInfo,
    msg: ExecuteMsg,
) -> StdResult<Response> {
    match msg {
        ExecuteMsg::Deposit { leaf_hash } => execute_deposit(deps, env, info, leaf_hash),
        ExecuteMsg::Withdraw {
            root,
            nullifier,
            proof,
        } => execute_withdraw(deps, env, info, root, nullifier, proof),
        ExecuteMsg::WithdrawViaRelayer {
            root,
            nullifier,
            proof,
            recipient,
            relayer_fee,
        } => execute_withdraw_via_relayer(
            deps, env, info, root, nullifier, proof, recipient, relayer_fee,
        ),
        ExecuteMsg::BatchWithdraw {
            roots,
            nullifiers,
            proofs,
        } => execute_batch_withdraw(deps, env, info, roots, nullifiers, proofs),
        ExecuteMsg::SubmitProofAttestation {
            root,
            nullifier,
            external_hash,
            proof_hash,
        } => execute_submit_proof_attestation(
            deps, info, root, nullifier, external_hash, proof_hash,
        ),
    }
}

#[entry_point]
pub fn query(deps: Deps, _env: Env, msg: QueryMsg) -> StdResult<Binary> {
    match msg {
        QueryMsg::GetRoot {} => {
            let root = CURRENT_ROOT.load(deps.storage)?;
            to_json_binary(&RootResponse {
                root: hex::encode(&root),
            })
        }
        QueryMsg::IsSpent { nullifier } => {
            let key =
                hex::decode(&nullifier).map_err(|_| StdError::generic_err("Invalid hex"))?;
            let is_spent = NULLIFIERS
                .may_load(deps.storage, key)?
                .unwrap_or(false);
            to_json_binary(&SpentResponse { is_spent })
        }
        QueryMsg::GetConfig {} => {
            let config = CONFIG.load(deps.storage)?;
            to_json_binary(&config)
        }
        QueryMsg::GetPath { leaf_index } => query_get_path(deps, leaf_index),
    }
}

