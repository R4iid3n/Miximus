fn query_get_path(deps: Deps, leaf_index: u64) -> StdResult<Binary> {
    let next_leaf = NEXT_LEAF_INDEX.load(deps.storage)?;
    if leaf_index >= next_leaf {
        return Err(StdError::generic_err("Leaf not yet inserted"));
    }

    let mut path = Vec::with_capacity(TREE_DEPTH);
    let mut address_bits = Vec::with_capacity(TREE_DEPTH);
    let mut idx = leaf_index;

    for level in 0..TREE_DEPTH {
        let is_right = (idx & 1) == 1;
        address_bits.push(is_right);
        let sibling_idx = idx ^ 1;
        let sibling = get_node(deps, level as u32, sibling_idx)?;
        let sibling_value = bytes_to_uint256(&sibling);
        path.push(sibling_value.to_string());
        idx >>= 1;
    }

    to_json_binary(&PathResponse {
        path,
        address_bits,
    })
}

// =========================================================================
//                 MERKLE TREE OPERATIONS (full node storage)
// =========================================================================

/// Initialize the Merkle tree by computing zero hashes for each level.
///
/// zero_hash[0] = 0 (empty leaf)
/// zero_hash[i] = MiMC_IV(level_i)(zero_hash[i-1], zero_hash[i-1])
///
/// The root of a completely empty tree is the final zero hash.
fn initialize_merkle_tree(deps: DepsMut) -> StdResult<Vec<u8>> {
    let mut zero = Uint256::zero();

    for level in 0..TREE_DEPTH {
        let zero_bytes = uint256_to_bytes(&zero);
        ZERO_HASHES.save(deps.storage, level as u32, &zero_bytes)?;

        // Hash two zero children at this level to get zero hash for next level
        let data = [zero, zero];
        zero = mimc_hash_with_level_iv(&data, level);
    }

    // The final zero is the root of a completely empty tree
    let root_bytes = uint256_to_bytes(&zero);
    Ok(root_bytes)
}

/// Insert a leaf into the Merkle tree at the given index, updating all
/// ancestor nodes up to the root. Returns the new root hash as bytes.
///
/// This stores EVERY computed node in TREE_NODES, matching the EVM contract's
/// treeNodes[level][index] = hash pattern.
fn insert_leaf(deps: DepsMut, leaf_index: u64, leaf_bytes: &[u8]) -> StdResult<Vec<u8>> {
    // Store the leaf at level 0
    TREE_NODES.save(deps.storage, (0u32, leaf_index), &leaf_bytes.to_vec())?;

    let mut current_node = bytes_to_uint256(leaf_bytes);
    let mut idx = leaf_index;

    for level in 0..TREE_DEPTH {
        let parent_idx = idx / 2;

        let (left, right) = if idx % 2 == 0 {
            // Current node is left child; sibling is right
            let sibling = get_node(deps.as_ref(), level as u32, idx + 1)?;
            (current_node, bytes_to_uint256(&sibling))
        } else {
            // Current node is right child; sibling is left
            let sibling = get_node(deps.as_ref(), level as u32, idx - 1)?;
            (bytes_to_uint256(&sibling), current_node)
        };

        let data = [left, right];
        current_node = mimc_hash_with_level_iv(&data, level);

        // Store the parent node at level+1
        let parent_bytes = uint256_to_bytes(&current_node);
        TREE_NODES.save(deps.storage, ((level + 1) as u32, parent_idx), &parent_bytes)?;

        idx = parent_idx;
    }

    Ok(uint256_to_bytes(&current_node))
}

/// Retrieve a node from the Merkle tree. If the node has not been stored
/// (i.e., it was never part of an insertion path), return the zero hash
/// for that level.
fn get_node(deps: Deps, level: u32, index: u64) -> StdResult<Vec<u8>> {
    match TREE_NODES.may_load(deps.storage, (level, index))? {
        Some(val) => Ok(val),
        None => {
            // Return the zero hash for this level (uninitialized node)
            ZERO_HASHES.load(deps.storage, level)
        }
    }
}

// =========================================================================
//                    PROOF VERIFICATION (oracle-based)
// =========================================================================

/// Compute external_hash = sha256(contract_address || recipient) mod SCALAR_FIELD.
/// This matches the EVM contract's getExtHash() which uses sha256.
fn compute_external_hash(contract_addr: &Addr, recipient: &Addr) -> Uint256 {
    use sha2::{Digest, Sha256};

    let p = scalar_field();
    let mut hasher = Sha256::new();
    hasher.update(contract_addr.as_bytes());
    hasher.update(recipient.as_bytes());
    let hash = hasher.finalize();
    bytes_to_uint256(&hash) % p
}

/// Compute the attestation key: keccak256(root || nullifier || external_hash || proof_data).
/// This key uniquely binds the oracle's attestation to a specific proof and public inputs.
fn compute_attestation_key(
    root: &[u8],
    nullifier: &[u8],
    external_hash: &[u8],
    proof_data: &[u8],
) -> [u8; 32] {
    let mut data = Vec::with_capacity(root.len() + nullifier.len() + external_hash.len() + proof_data.len());
    data.extend_from_slice(root);
    data.extend_from_slice(nullifier);
    data.extend_from_slice(external_hash);
    data.extend_from_slice(proof_data);
    keccak256(&data)
}

/// Verify a zkSNARK proof by checking if the trusted oracle has submitted
/// an attestation for this specific proof and public inputs.
///
/// CosmWasm does not have native BN254 pairing precompiles, so we cannot
/// perform on-chain Groth16 verification. Instead, a trusted oracle performs
/// the pairing check off-chain and submits an attestation to the contract.
///
/// The verification flow:
/// 1. User generates a Groth16 proof off-chain
/// 2. User submits the proof to the trusted oracle service
/// 3. Oracle verifies the BN254 pairing check (using snarkjs/bellman/etc.)
/// 4. If valid, oracle calls SubmitProofAttestation with the public inputs + proof hash
/// 5. User calls Withdraw/WithdrawViaRelayer; this function checks the attestation exists
fn verify_proof(
    deps: Deps,
    root: &[u8],
    nullifier: &[u8],
    external_hash: &[u8],
    proof: &[u8],
) -> StdResult<bool> {
    // Hash the proof data to get a compact key component
    let proof_hash = keccak256(proof);

    // Compute the attestation key
    let attestation_key = compute_attestation_key(root, nullifier, external_hash, &proof_hash);

    // Check if the oracle has attested this proof
    let attested = PROOF_ATTESTATIONS
        .may_load(deps.storage, attestation_key.to_vec())?
        .unwrap_or(false);

    Ok(attested)
}
