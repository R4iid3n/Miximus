fn execute_deposit(
    deps: DepsMut,
    _env: Env,
    info: MessageInfo,
    leaf_hash: String,
) -> StdResult<Response> {
    let config = CONFIG.load(deps.storage)?;

    // Verify correct amount was sent
    let sent = info
        .funds
        .iter()
        .find(|c| c.denom == config.denom)
        .map(|c| c.amount)
        .unwrap_or(Uint128::zero());

    if sent != config.denomination {
        return Err(StdError::generic_err("Must deposit exact denomination"));
    }

    let leaf_index = NEXT_LEAF_INDEX.load(deps.storage)?;
    if leaf_index >= MAX_LEAVES {
        return Err(StdError::generic_err("Merkle tree is full"));
    }

    // Parse leaf hash as a decimal string (field element) and convert to 32-byte big-endian
    let leaf_value = uint256_from_dec(&leaf_hash);
    let leaf_bytes = uint256_to_bytes(&leaf_value);

    // Insert leaf into Merkle tree and compute new root
    let new_root = insert_leaf(deps, leaf_index, &leaf_bytes)?;

    // Update state
    NEXT_LEAF_INDEX.save(deps.storage, &(leaf_index + 1))?;
    CURRENT_ROOT.save(deps.storage, &new_root)?;
    ROOTS.save(deps.storage, new_root, &true)?;

    Ok(Response::new()
        .add_attribute("method", "deposit")
        .add_attribute("leaf_index", leaf_index.to_string())
        .add_attribute("leaf_hash", leaf_hash))
}

fn execute_batch_deposit(
    mut deps: DepsMut,
    _env: Env,
    info: MessageInfo,
    leaf_hashes: Vec<String>,
) -> StdResult<Response> {
    let config = CONFIG.load(deps.storage)?;

    let count = leaf_hashes.len() as u128;
    if count == 0 || count > 20 {
        return Err(StdError::generic_err("Batch size must be 1-20"));
    }

    // Verify total amount sent
    let sent = info
        .funds
        .iter()
        .find(|c| c.denom == config.denom)
        .map(|c| c.amount)
        .unwrap_or(Uint128::zero());

    let total_required = config.denomination * Uint128::from(count);
    if sent != total_required {
        return Err(StdError::generic_err("Must deposit exact total denomination"));
    }

    let mut leaf_index = NEXT_LEAF_INDEX.load(deps.storage)?;
    let start_index = leaf_index;

    for leaf_hash in &leaf_hashes {
        if leaf_index >= MAX_LEAVES {
            return Err(StdError::generic_err("Merkle tree is full"));
        }

        let leaf_value = uint256_from_dec(leaf_hash);
        let leaf_bytes = uint256_to_bytes(&leaf_value);
        let new_root = insert_leaf(deps.branch(), leaf_index, &leaf_bytes)?;

        leaf_index += 1;
        NEXT_LEAF_INDEX.save(deps.storage, &leaf_index)?;
        CURRENT_ROOT.save(deps.storage, &new_root)?;
        ROOTS.save(deps.storage, new_root, &true)?;
    }

    Ok(Response::new()
        .add_attribute("method", "batch_deposit")
        .add_attribute("start_index", start_index.to_string())
        .add_attribute("count", count.to_string()))
}

fn execute_withdraw(
    deps: DepsMut,
    env: Env,
    info: MessageInfo,
    root: String,
    nullifier: String,
    proof: Vec<u8>,
) -> StdResult<Response> {
    let config = CONFIG.load(deps.storage)?;
    let recipient = info.sender.clone();

    let nullifier_bytes =
        hex::decode(&nullifier).map_err(|_| StdError::generic_err("Invalid nullifier hex"))?;
    let root_bytes =
        hex::decode(&root).map_err(|_| StdError::generic_err("Invalid root hex"))?;

    // Check nullifier not spent
    if NULLIFIERS
        .may_load(deps.storage, nullifier_bytes.clone())?
        .unwrap_or(false)
    {
        return Err(StdError::generic_err("Cannot double-spend"));
    }

    // Check root is known
    if !ROOTS
        .may_load(deps.storage, root_bytes.clone())?
        .unwrap_or(false)
    {
        return Err(StdError::generic_err("Unknown merkle root"));
    }

    // Compute external hash: sha256(contract_address || recipient) mod SCALAR_FIELD
    let external_hash = compute_external_hash(&env.contract.address, &recipient);

    // Verify proof via oracle attestation
    let is_valid = verify_proof(
        deps.as_ref(),
        &root_bytes,
        &nullifier_bytes,
        &uint256_to_bytes(&external_hash),
        &proof,
    )?;
    if !is_valid {
        return Err(StdError::generic_err("Invalid zkSNARK proof: no oracle attestation found"));
    }

    // Mark nullifier as spent
    NULLIFIERS.save(deps.storage, nullifier_bytes, &true)?;

    // Transfer denomination to recipient
    let transfer_msg = CosmosMsg::Bank(BankMsg::Send {
        to_address: recipient.to_string(),
        amount: vec![Coin {
            denom: config.denom,
            amount: config.denomination,
        }],
    });

    Ok(Response::new()
        .add_message(transfer_msg)
        .add_attribute("method", "withdraw")
        .add_attribute("nullifier", nullifier)
        .add_attribute("recipient", recipient))
}

fn execute_batch_withdraw(
    deps: DepsMut,
    env: Env,
    info: MessageInfo,
    roots: Vec<String>,
    nullifiers: Vec<String>,
    proofs: Vec<Vec<u8>>,
) -> StdResult<Response> {
    let config = CONFIG.load(deps.storage)?;
    let recipient = info.sender.clone();
    let count = roots.len();

    if count == 0 || count > 5 {
        return Err(StdError::generic_err("Batch size must be 1-5"));
    }
    if nullifiers.len() != count || proofs.len() != count {
        return Err(StdError::generic_err("Array lengths must match"));
    }

    for i in 0..count {
        let nullifier_bytes = hex::decode(&nullifiers[i])
            .map_err(|_| StdError::generic_err("Invalid nullifier hex"))?;
        let root_bytes = hex::decode(&roots[i])
            .map_err(|_| StdError::generic_err("Invalid root hex"))?;

        // Check nullifier not spent
        if NULLIFIERS
            .may_load(deps.storage, nullifier_bytes.clone())?
            .unwrap_or(false)
        {
            return Err(StdError::generic_err("Cannot double-spend"));
        }

        // Check root is known
        if !ROOTS
            .may_load(deps.storage, root_bytes.clone())?
            .unwrap_or(false)
        {
            return Err(StdError::generic_err("Unknown merkle root"));
        }

        // Compute external hash
        let external_hash = compute_external_hash(&env.contract.address, &recipient);

        // Verify proof via oracle attestation
        let is_valid = verify_proof(
            deps.as_ref(),
            &root_bytes,
            &nullifier_bytes,
            &uint256_to_bytes(&external_hash),
            &proofs[i],
        )?;
        if !is_valid {
            return Err(StdError::generic_err("Invalid zkSNARK proof: no oracle attestation found"));
        }

        // Mark nullifier as spent
        NULLIFIERS.save(deps.storage, nullifier_bytes, &true)?;
    }

    // Transfer total denomination to recipient
    let total_amount = config.denomination * Uint128::from(count as u128);
    let transfer_msg = CosmosMsg::Bank(BankMsg::Send {
        to_address: recipient.to_string(),
        amount: vec![Coin {
            denom: config.denom,
            amount: total_amount,
        }],
    });

    Ok(Response::new()
        .add_message(transfer_msg)
        .add_attribute("method", "batch_withdraw")
        .add_attribute("count", count.to_string())
        .add_attribute("recipient", recipient))
}

fn execute_withdraw_via_relayer(
    deps: DepsMut,
    env: Env,
    info: MessageInfo,
    root: String,
    nullifier: String,
    proof: Vec<u8>,
    recipient: String,
    relayer_fee: Uint128,
) -> StdResult<Response> {
    let config = CONFIG.load(deps.storage)?;
    let recipient_addr = deps.api.addr_validate(&recipient)?;

    if relayer_fee >= config.denomination {
        return Err(StdError::generic_err("Fee exceeds denomination"));
    }

    let nullifier_bytes =
        hex::decode(&nullifier).map_err(|_| StdError::generic_err("Invalid nullifier hex"))?;
    let root_bytes =
        hex::decode(&root).map_err(|_| StdError::generic_err("Invalid root hex"))?;

    if NULLIFIERS
        .may_load(deps.storage, nullifier_bytes.clone())?
        .unwrap_or(false)
    {
        return Err(StdError::generic_err("Cannot double-spend"));
    }
    if !ROOTS
        .may_load(deps.storage, root_bytes.clone())?
        .unwrap_or(false)
    {
        return Err(StdError::generic_err("Unknown merkle root"));
    }

    // Compute external hash: sha256(contract_address || recipient) mod SCALAR_FIELD
    let external_hash = compute_external_hash(&env.contract.address, &recipient_addr);

    let is_valid = verify_proof(
        deps.as_ref(),
        &root_bytes,
        &nullifier_bytes,
        &uint256_to_bytes(&external_hash),
        &proof,
    )?;
    if !is_valid {
        return Err(StdError::generic_err("Invalid proof: no oracle attestation found"));
    }

    NULLIFIERS.save(deps.storage, nullifier_bytes, &true)?;

    let mut messages: Vec<CosmosMsg> = vec![];

    // Relayer fee
    if !relayer_fee.is_zero() {
        messages.push(CosmosMsg::Bank(BankMsg::Send {
            to_address: info.sender.to_string(),
            amount: vec![Coin {
                denom: config.denom.clone(),
                amount: relayer_fee,
            }],
        }));
    }

    // Recipient gets remainder
    let remaining = config.denomination - relayer_fee;
    messages.push(CosmosMsg::Bank(BankMsg::Send {
        to_address: recipient,
        amount: vec![Coin {
            denom: config.denom,
            amount: remaining,
        }],
    }));

    Ok(Response::new()
        .add_messages(messages)
        .add_attribute("method", "withdraw_via_relayer")
        .add_attribute("nullifier", nullifier))
}

/// Submit a Groth16 proof attestation. Only the trusted oracle can call this.
///
/// The oracle performs the full BN254 pairing check off-chain (using a Groth16 verifier
/// library such as snarkjs, ethsnarks, or bellman) and then submits an attestation
/// that the proof is valid for the given public inputs.
///
/// The attestation key is keccak256(root || nullifier || external_hash || proof_hash),
/// which uniquely identifies the proof + public inputs combination.
fn execute_submit_proof_attestation(
    deps: DepsMut,
    info: MessageInfo,
    root: String,
    nullifier: String,
    external_hash: String,
    proof_hash: String,
) -> StdResult<Response> {
    let config = CONFIG.load(deps.storage)?;

    // Only the trusted oracle may submit attestations
    if info.sender != config.oracle {
        return Err(StdError::generic_err(
            "Unauthorized: only the oracle can submit proof attestations",
        ));
    }

    let root_bytes =
        hex::decode(&root).map_err(|_| StdError::generic_err("Invalid root hex"))?;
    let nullifier_bytes =
        hex::decode(&nullifier).map_err(|_| StdError::generic_err("Invalid nullifier hex"))?;
    let external_hash_bytes = hex::decode(&external_hash)
        .map_err(|_| StdError::generic_err("Invalid external_hash hex"))?;
    let proof_hash_bytes =
        hex::decode(&proof_hash).map_err(|_| StdError::generic_err("Invalid proof_hash hex"))?;

    // Compute attestation key: keccak256(root || nullifier || external_hash || proof_hash)
    let attestation_key = compute_attestation_key(
        &root_bytes,
        &nullifier_bytes,
        &external_hash_bytes,
        &proof_hash_bytes,
    );

    PROOF_ATTESTATIONS.save(deps.storage, attestation_key.to_vec(), &true)?;

    Ok(Response::new()
        .add_attribute("method", "submit_proof_attestation")
        .add_attribute("root", root)
        .add_attribute("nullifier", nullifier))
}

